  function applyLang(lang) {
    document.documentElement.lang = (lang === "ur") ? "ur" : "en";
    document.body.classList.toggle("ur", lang === "ur");

    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
      const val = dict[lang]?.[key];
      if (val) el.textContent = val;
    });

    const btnEN = document.getElementById("langEN");
    const btnUR = document.getElementById("langUR");
    if (btnEN && btnUR) {
      btnEN.classList.toggle("active", lang === "en");
      btnUR.classList.toggle("active", lang === "ur");
      btnEN.onclick = () => { localStorage.setItem("aspi_lang", "en"); applyLang("en"); };
      btnUR.onclick = () => { localStorage.setItem("aspi_lang", "ur"); applyLang("ur"); };
    }
  }

  applyLang(localStorage.getItem("aspi_lang") || "en");
